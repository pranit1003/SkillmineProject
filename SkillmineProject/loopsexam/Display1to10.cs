using System;
using System.Collections.Generic;
using System.Text;

namespace SkillmineProject.loopsexam
{
    class Display1to10
    {
        static void Main(string[] args)
        {
            for(int i=1; i<=10; i++)
            {
                Console.WriteLine(i);
            }


            for(int i=120 ; i<=200; i++)
            {
                Console.WriteLine(i);
            }

            for (int i = 75; i >= 65; i--)
            {
                Console.WriteLine(i);
            }


        }


    }
}
